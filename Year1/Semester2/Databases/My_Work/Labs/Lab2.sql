--EX 11
SELECT FIRST_NAME || ' ' || LAST_NAME AS "FULL NAME", HIRE_DATE, NEXT_DAY(HIRE_DATE + INTERVAL '6' MONTH, 'MONDAY') AS "NEGOCIERE"
FROM EMPLOYEES;

--EX 12
SELECT
    LAST_NAME AS "Nume",
    ROUND(MONTHS_BETWEEN(SYSDATE, HIRE_DATE)) AS "Luni lucrate"
FROM EMPLOYEES
ORDER BY "Luni lucrate";

--EX 13
SELECT
    LAST_NAME AS "Nume",
    HIRE_DATE AS "Data Angajării",
    TO_CHAR(HIRE_DATE, 'Day') AS "Zi",
    CASE
        WHEN TO_CHAR(HIRE_DATE, 'D') = '2' THEN 1 -- Luni
        WHEN TO_CHAR(HIRE_DATE, 'D') = '3' THEN 2 -- Marți
        WHEN TO_CHAR(HIRE_DATE, 'D') = '4' THEN 3 -- Miercuri
        WHEN TO_CHAR(HIRE_DATE, 'D') = '5' THEN 4 -- Joi
        WHEN TO_CHAR(HIRE_DATE, 'D') = '6' THEN 5 -- Vineri
        WHEN TO_CHAR(HIRE_DATE, 'D') = '7' THEN 6 -- Sâmbătă
        WHEN TO_CHAR(HIRE_DATE, 'D') = '1' THEN 7 -- Duminică
        END AS "Ordine"
FROM EMPLOYEES
ORDER BY "Ordine";

--EX 14
