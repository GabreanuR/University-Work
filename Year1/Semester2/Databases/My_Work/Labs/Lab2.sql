SELECT CONCAT(FIRST_NAME, ' ') || CONCAT(LAST_NAME, ' ') || 'castiga ' || SALARY || ' lunar dar doreste ' || SALARY * 3 AS "Salariul ideal"
FROM EMPLOYEES;

