--EX 15
SELECT
    LAST_NAME AS "Nume",
    NVL(TO_CHAR(COMMISSION_PCT * 100), 'Fara_comision') AS "Comision"
FROM EMPLOYEES;

--EX 16
SELECT LAST_NAME, SALARY, COMMISSION_PCT
FROM EMPLOYEES
WHERE SALARY + NVL(SALARY * COMMISSION_PCT, 0) > 10000;

--EX 17
SELECT LAST_NAME,
       JOB_ID,
       SALARY,
       CASE
           WHEN JOB_ID = 'IT_PROG' THEN SALARY * 1.20
           WHEN JOB_ID = 'SA_REP'  THEN SALARY * 1.25
           WHEN JOB_ID = 'SA_MAN'  THEN SALARY * 1.35
           ELSE SALARY
           END AS "SALARIU DUPĂ MĂRIRE"
FROM EMPLOYEES;

