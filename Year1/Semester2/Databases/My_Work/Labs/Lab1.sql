FROM USER_TAB_COLUMNS
WHERE TABLE_NAME IN ('EMPLOYEES', 'DEPARTMENTS', 'JOBS', 'JOB_HISTORY', 'LOCATIONS', 'COUNTRIES', 'REGIONS')
