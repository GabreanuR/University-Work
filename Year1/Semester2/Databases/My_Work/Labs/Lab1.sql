--EX 11
SELECT FIRST_NAME, LAST_NAME, JOB_ID, HIRE_DATE
FROM EMPLOYEES
WHERE HIRE_DATE BETWEEN TO_DATE('20-02-1987', 'DD-MM-YYYY')
          AND TO_DATE('01-05-1989', 'DD-MM-YYYY')
ORDER BY HIRE_DATE;

